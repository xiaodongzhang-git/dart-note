void main(List<String> args) {
  String color = 'blue';
  switch (color) {
    case 'red':
      print('The color is red');
      break;
    case 'green':
      print('The color is green');
      break;
    case 'blue':
      print('The color is blue');
      break;
    default:
      print('Unknown color');
  }
}